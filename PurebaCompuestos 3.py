print("===============================")

numUno=int(input("Cual es el número que deseas convertir?: "))

if numUno == 1:
    print("El número es el 'Uno'")
elif numUno == 2:
    print("El número es el 'Dos'")
elif numUno == 3:
    print("El número es el 'Tres'")
elif numUno == 4:
    print("El número es el 'Cuatro'")
elif numUno == 5:
    print("El número es el 'Cinco'")
else:
    print("Este programa solo puede convertir hasta el número 5")

print("Fin.")
